# Passo 1: Solicitar ao usuário que digite a base do retângulo
base = float(input("Digite a base do retângulo: "))

# Passo 2: Solicitar ao usuário que digite a altura do retângulo
altura = float(input("Digite a altura do retângulo: "))

# Passo 3: Calcular a área do retângulo
area = base * altura

# Passo 4: Exibir a área do retângulo
print(f"A área do retângulo é {area}")
